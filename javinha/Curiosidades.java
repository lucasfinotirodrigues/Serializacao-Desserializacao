package javinha;

public class Curiosidades {
    private String resumo;
    private String fontePrimaria;
    private String fonteSecundaria;

    public String getResumo() {
        return resumo;
    }
    public void setResumo(String resumo) {
        this.resumo = resumo;
    }
    public String getFontePrimaria() {
        return fontePrimaria;
    }
    public void setFontePrimaria(String fontePrimaria) {
        this.fontePrimaria = fontePrimaria;
    }
    public String getFonteSecundaria() {
        return fonteSecundaria;
    }
    public void setFonteSecundaria(String fonteSecundaria) {
        this.fonteSecundaria = fonteSecundaria;
    }
}
