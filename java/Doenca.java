package java;

public class Doenca {
    private String nome;
    private Informacoes informacoes;
    private Curiosidades curiosidades;

    public String getNome() {
        return nome;    
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public Informacoes getInformacoes() {
        return informacoes;
    }
    public void setInformacoes(Informacoes informacoes) {
        this.informacoes = informacoes;
    }
    public Curiosidades getCuriosidades() {
        return curiosidades;
    }
    public void setCuriosidades(Curiosidades curiosidades) {
        this.curiosidades = curiosidades;
    }
}
